    private void CalibraGuardarCambios(){
        String Estado = UsoBaseDeDatos.GuardaCambiosPoblacion(jLPoblacionSeleccionada.getText(), jLCodigoPostalSeleccionada.getText(),  jTFCalibracionLatitud.getText(), jTFCalibracionLongitud.getText(), jTACalibracionReferencia.getText());
        if(Estado.equals("OK")){
            JOptionPane.showMessageDialog(this, "MODIFICACION EXITOSA", "MODIFICACION",JOptionPane.INFORMATION_MESSAGE,IconoHecho);
            jDPoblaciones.dispose();
            LanzoVentanaPoblaciones();
            jDCalibraPoblacion.dispose();
            jTAConsola.append("[Fecha "+Fecha()+" Hora "+Hora()+"] - Poblaciones:\n    Modificacion exitosa en "+ jLPoblacionSeleccionada.getText() + "\n");
        }else{
            JOptionPane.showMessageDialog(this, "MODIFICACION FALLIDA \n"+Estado, "MODIFICACION",JOptionPane.INFORMATION_MESSAGE,IconoError);
            jTAConsola.append("[Fecha "+Fecha()+" Hora "+Hora()+"] - Poblaciones:\n    Modificacion fallida en "+ jLPoblacionSeleccionada.getText() + "\n" + Estado + "\n");

        }
    }

