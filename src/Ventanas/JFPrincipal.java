    private void BuscarNuevaInformacion(){
        if(jRBCodigoPostal.isSelected()){
            String InformacionMpas[] = GoogleMaps.GetPoblacionPlaces(jCBPoblaciones.getSelectedItem().toString(),jLSCodigoPostal.getText());
            jTFCalibracionLatitud.setText(InformacionMpas[0]);
            jTFCalibracionLongitud.setText(InformacionMpas[1]);
            jTACalibracionReferencia.setText(InformacionMpas[2]);
        }else if(jRBCordendas.isSelected()){
            String InformacionMpas[] = GoogleMaps.GetCordenadasPlaces(jTFCalibraCoordenadas.getText());
            jTFCalibracionLatitud.setText(InformacionMpas[0]);
            jTFCalibracionLongitud.setText(InformacionMpas[1]);
            jTACalibracionReferencia.setText(InformacionMpas[2]);
        }
    }

