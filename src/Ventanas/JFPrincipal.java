    private String VehiculosValidaCampos(){
        String Status = "";
        String Modelo = tfVehiculosMadelo.getText();
        String Marca = tfVehiculosMarca.getText();
        String Consumo = tfVehiculosRendimiento.getText();

        Pattern pat = Pattern.compile("[1-9]+[0-9]*");//<-----------Define numero entero
        Matcher mat = pat.matcher(Consumo);

        if(Modelo.equals("") | Marca.equals("") | Consumo.equals("")){
            Status = "CAMPOS BACIOS";
        }
        if(Modelo.length()>30){
            Status = "Nombre de modelo demaciado largo (30 caracteres)\n";
        }
        if(Marca.length()>30){
            Status = Status + "Nombre de marca demaciado largo (30 caracteres)\n";
        }
        if(Consumo.length()>30){
            Status = Status + "Contenido de rendimiento demaciado largo (3 caracteres)\n";
        }
        if(!mat.matches()){
            Status = Status = "Formato inadecuado para campo rendimiento \n";
        }
        if(UsoBaseDeDatos.VehiculoRepetido(Modelo)){
            Status = Status = "Vehiculo ya registrado \n";
        }
        if(Status.equals("")){
            Status = "Ok";
        }
        return Status;
    }

