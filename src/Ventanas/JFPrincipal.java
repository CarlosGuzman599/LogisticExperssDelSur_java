    private static String Hora(){
        Calendar calendario = Calendar.getInstance();
        int Hora, Minutos, Segundos;
        Hora = calendario.get(Calendar.HOUR_OF_DAY);
        Minutos = calendario.get(Calendar.MINUTE);
        Segundos = calendario.get(Calendar.SECOND);
        String HoraCadena = Hora+":"+Minutos+":"+Segundos;
        return HoraCadena;
    }

    private static String Fecha(){
        Calendar calendario = Calendar.getInstance();
        int Dia, Mes, Ano;
        Dia = calendario.get(Calendar.DAY_OF_MONTH);
        Mes = calendario.get(Calendar.MONTH)+1;
        Ano = calendario.get(Calendar.YEAR);
        String FechaCadena = Dia+"/"+Mes+"/"+Ano;
        return FechaCadena;
    }

    private void AgregarServicios() throws SQLException{
        String Status = "";
        String Ruta = "";
        JFileChooser FC = new JFileChooser();
        int Op = FC.showOpenDialog(FC);

        if(Op == JFileChooser.APPROVE_OPTION){
            Ruta = FC.getSelectedFile().getPath();
            Ruta = DepuraDatos.CambiaDiagonal(Ruta);
            jTAConsola.append("[Fecha "+Fecha()+" Hora "+Hora()+"] - Agregar Servicios:\n    "+ UsoBaseDeDatos.AgregarExcel(Ruta)+ "\n");
            jLServiciosPendientes.setText(UsoBaseDeDatos.ConteoServiciosAtender()+"");
            jTAConsola.append("-   Poblaciones: "+UsoBaseDeDatos.ConteoPoblacionesErroneas()+" Poblaciones con informacion incompleta \n");
            jTAConsola.append("-   Clientes: "+UsoBaseDeDatos.ConteoClientesErroneos()+" Clientes con informacion incompleta \n");
        }else{
            jTAConsola.append("[Fecha "+Fecha()+" Hora "+Hora()+"] - Agregar Servicios: Sin Acciones \n");
            jTAConsola.append("-   Poblaciones: "+UsoBaseDeDatos.ConteoPoblacionesErroneas()+" Poblaciones con informacion incompleta \n");
        }
    }


